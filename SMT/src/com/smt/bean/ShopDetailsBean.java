package com.smt.bean;

public class ShopDetailsBean
{
	private String pkShopId;
	private String shopName;
	private String contactNumber;
	private String shopAddress;
	private String city;
	private String shopRegDate;
	private String gstNo;
	
	public String getPkShopId() {
		return pkShopId;
	}
	public void setPkShopId(String pkShopId) {
		this.pkShopId = pkShopId;
	}
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public String getContactNumber() {
		return contactNumber;
	}
	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}
	public String getShopAddress() {
		return shopAddress;
	}
	public void setShopAddress(String shopAddress) {
		this.shopAddress = shopAddress;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getShopRegDate() {
		return shopRegDate;
	}
	public void setShopRegDate(String shopRegDate) {
		this.shopRegDate = shopRegDate;
	}
	public String getGstNo() {
		return gstNo;
	}
	public void setGstNo(String gstNo) {
		this.gstNo = gstNo;
	}
}
